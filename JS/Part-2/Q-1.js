// print the day of the week like monday if day is 1 or tuesday is day is 2 and so on

let day = 2;

switch(day){

    case 1 :
        console.log("Monday");
        break;
    case 2 :
        console.log("Tuesday");
        break;
        case 3 :
        console.log("wednesday");
        break;
        case 4 :
        console.log("Thursday");
        break;
        case 5 :
        console.log("Friday");
        break;
        case 6 :
        console.log("saturday");
        break;   
        case 7 :
        console.log("sunday");
        break;
        default:
            console.log("not found");
}